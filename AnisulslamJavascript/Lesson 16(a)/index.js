//digit spelling
// 0 - Zero, 1 - One...........9 - nine, 1- - Not valid digit
//Switch, case, break, default

var digit = prompt("Enter any digit : ");
if(digit == 0)
    document.write("Zero");

else if (digit == 1)
    document.write("One");

else if (digit == 2)
    document.write("Two");

else if (digit == 3)
    document.write("Three");

else if (digit == 4)
    document.write("Four");

else if (digit == 5)
    document.write("Five");

else if (digit == 6)
    document.write("Six");

else if (digit == 7)
    document.write("Seven");

else if (digit == 8)
    document.write("Eight");

else if (digit == 9)
    document.write("Nine");

else
    document.write("Not a Digit");

