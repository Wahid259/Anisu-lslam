
package string_package;

public class StringDemo13 {
    public static void main(String[] args) {
        
        String s = "32";
        int i = Integer.parseInt(s);
        System.out.println("i = "+i);
        
        
        String t = "32.25";
        double j = Double.parseDouble(t);
        System.out.println("j = "+j);
        
        
        
    }
}
