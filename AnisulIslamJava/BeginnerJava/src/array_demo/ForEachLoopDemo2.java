
package array_demo;

public class ForEachLoopDemo2 {
    public static void main(String[] args) {
        
        String[] name = {"Wahid", "Komey", "Umme", "Brinto"};
        
        System.out.println(name.length);
        for (int i = 0; i < 4; i++) {
            System.out.println(name[i]);
            
        }
        
    }
}
