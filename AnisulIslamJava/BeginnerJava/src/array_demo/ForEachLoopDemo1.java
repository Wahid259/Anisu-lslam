
package array_demo;

public class ForEachLoopDemo1 {
    public static void main(String[] args) {
        
        String[] name = new String[4];
        name[0] = "Wahid";
        name[1] = "Komey";
        name[2] = "Umme";
        name[3] = "Brinto";
        
        for (int i = 0; i < 4; i++) {
            System.out.println(name[i]);
            
        }
        
    }
    
}
