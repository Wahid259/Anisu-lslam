package beginnerjava;

public class WhileLoopDemo {
    public static void main(String[] args) {
        
        int i = 1;
        
        while(i<=10){
            System.out.println(i+" Bangladesh"); // 1 to 10 print using while loop
            i++;
        }
        System.out.println("\n \n \n \n");
        
        int j = 2;
        
        while(j<=100){
            System.out.println(j+" Bangladesh");//Even number print
            j = j + 2;
        }
        
        System.out.println("\n \n \n \n");
        
        int k = 1;
        
        while(k<=100){
            System.out.println(k+" Bangladesh");//Odd number print
            k = k + 2;
        }
    }
    
}
