
package beginnerjava;

public class Loop1Demo {
    
    public static void main(String[] args) {
        
        int sum = 0;
        // 1 to 10 er sum dakhabo
        for (int i = 1; i <= 10; i++) {
            
            sum = sum + i;
            
        }
        System.out.println("The Sum is = "+sum);
        
    }
    
}
